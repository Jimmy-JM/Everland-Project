package 에버랜드;

public class Everland5Array {
	public String ticket = null;
	public String preferential = null;
	public String bigSmall = null;
	public int price = 0;
	public int order = 0;
	public String coupon = null;
	
	public Everland5Array(String ticket, String preferential, String bigSmall, int price, int order, String coupon) {
		this.ticket = ticket;
		this.preferential = preferential;
		this.bigSmall = bigSmall;
		this.price = price;
		this.order = order;
		this.coupon = coupon; 
	}
}
